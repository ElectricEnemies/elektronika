Atransmorph::Atransmorph(QIID qiid, char *name, Aobject *f, int x, int y) : Atransition(qiid, name, f, x, y)
{
	Atable *table=Atable::getTable(this);
	char	str[1024];
	int		i;
	init(table->rootdir);
	
	{
		Aeffect *e=Aeffect::getEffect(this);
		int		w=e->getVideoWidth()*2;
		int		h=e->getVideoHeight();
		sources=new Abitmap(w*2, h);
	}
	
	bChange=false;
	
	for(i=0; i<16; i++)
		bmp[i]=new Abitmap(4, 4);
	
	wipe=null;
	nwipe=-1;
	
	items=new Aitem("items", "items");
	{
		int	i;
		for(i=0; i<ndir; i++)
		{
			char *s=strrchr(dir[i], '\\')+1;
			if(s)
				new Aitem(s, s, items, i);
		}
	}
	
	list=new Alist("wipes", this, 4, 78, pos.w-42, 14, items);
	list->setTooltips("wipes");
	list->show(true);
	list->setCurrentByData(0);
	
	flipx=new ActrlButton(MKQIID(qiid, 0x8f0456a12c039bcc), "flipX", this, 86, 78, 14, 14, &resource.get(MAKEINTRESOURCE(PNG_VMIX_FLIPX), "PNG"), Abutton::bt2STATES|Abutton::btBITMAP);
	flipx->setTooltips("flip X");
	flipx->set(false);
	flipx->show(true);
	
	flipy=new ActrlButton(MKQIID(qiid, 0x8f0456a12c039bad), "flipX", this, 102, 78, 14, 14, &resource.get(MAKEINTRESOURCE(PNG_VMIX_FLIPY), "PNG"), Abutton::bt2STATES|Abutton::btBITMAP);
	flipy->setTooltips("flip X");
	flipy->set(false);
	flipy->show(true);
	
	sprintf(str, "%s/mask", name);
	snap=new Asnap2(MKQIID(qiid, 0x5d286623dbee24c0), str, this, 4, 4, pos.w-8, 72);
	snap->setTooltips("mask select");
	snap->set(0);
	snap->show(TRUE);
	
	change();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Atransmorph::~Atransmorph()
{
	int	i;
	for(i=0; i<16; i++)
		delete(bmp[i]);
	delete(list);
	delete(items);
	if(wipe)
		delete(wipe);
	delete(snap);
	delete(flipx);
	delete(flipy);
	delete(sources);
	end();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Atransmorph::notify(Anode *o, int event, dword p)
{
	switch(event)
	{
		case nyCHANGE:
		if(o==list)
		{
			change();
			snap->repaint();
		}
		else if((o==flipx)||(o==flipy))
		{
			bChange=true;
		}
		break;
	}
	return Aobject::notify(o, event, p);
}

