/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Atransmorph::load(class Afile *f)
{
	char	file[ALIB_PATH];
	int		i;
	int		c=0;
	f->readString(file);
	for(i=0; i<ndir; i++)
	{
		char	*s=strrchr(dir[i], '\\');
		if(!strcmp(s, file))
		{
			c=i;
			break;
		}
	}
	list->setCurrentByData(c);
	change();
	return true;
}

