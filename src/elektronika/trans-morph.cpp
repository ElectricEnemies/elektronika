class Asnap2 : public AcontrolObj
{
public:
	AOBJ

								Asnap2							(QIID qiid, char *name, Aobject *L, int x, int y, int w, int h);
	virtual						~Asnap2							();

	virtual void				pulse							();
	virtual bool				mouse							(int x, int y, int state, int event);
	virtual bool				keyboard						(int event, int ascii, int scan, int state);

	virtual bool				sequence						(int nctrl, float value);
	
	virtual void				setBitmap						(int n, Abitmap *b);

	virtual void				set								(int p);
	virtual int					get								();

	virtual void				paint							(Abitmap *b);

	Acontrol					*control;

private:
	Abitmap						*bmp[16];
	int							ipos;
	int							defpos;
	
	bool						ctrlPaint;
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

ACI								Asnap2::CI		= ACI("Asnap",	GUID(0x11111112,0xA0000042), &Aobject::CI, 0, NULL);

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Asnap2::Asnap2(QIID qiid, char *name, Aobject *L, int x, int y, int w, int h) : AcontrolObj(name, L, x, y, w, h)
{
	int	i;
	for(i=0; i<16; i++)
		bmp[i]=new Abitmap(24, 14);
	control=new Acontrol(qiid, name, Aeffect::getEffect(L), this, Acontrol::KEY_16);
	ctrlPaint=false;
	set(0);
	defpos=-1;
	timer(100);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Asnap2::~Asnap2()
{
	int	i;
	delete(control);
	for(i=0; i<16; i++)
		delete(bmp[i]);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Asnap2::keyboard(int event, int ascii, int scan, int state)
{
	switch(event)
	{
		case keyboardDOWN:
		if(state&keyboardALT)
		{
			if(defpos!=-1)
				set(defpos);
			else
				set(0);
			father->notify(this, nyCHANGE);
			return true;
		}
		break;
	}
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Asnap2::mouse(int x, int y, int state, int event)
{
	switch(event)
	{
		case mouseNORMAL:
		case mouseLDOWN:
		case mouseLUP:
		cursor(cursorHANDSEL);
		focus(this);
		{
			if(getKeyboardState()&keyboardLALT)
			{
				if(defpos!=-1)
					set(defpos);
				else
					set(0);
				father->notify(this, nyCHANGE);
				return true;
			}
		}
		switch(event)
		{
			case mouseLDOWN:
			mouseCapture(true);
			return true;
			
			case mouseLUP:
			{
				int	xx=(x-X)/28;
				int	yy=(y-Y)/18;
				if((xx>=0)&&(xx<4)&&(yy>=0)&&(yy<4))
				{
					set(yy*4+xx);
					father->notify(this, nyCHANGE);
					repaint();
				}
				mouseCapture(false);
				return TRUE;
			}
			break;
		}
		break;
	}
	return FALSE;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Asnap2::set(int v)
{
	ipos=maxi(mini(v, 15), 0);
	if(defpos==-1)
		defpos=ipos;
	control->set(Acontrol::KEY, (float)ipos);
	repaint();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Asnap2::get()
{
	return ipos;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Asnap2::sequence(int nctrl, float value)
{
	ipos=(int)value;
	ipos=maxi(mini(ipos, 15), 0);
	ctrlPaint=true;
	father->notify(this, nyCHANGE);
	return true;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Asnap2::setBitmap(int n, Abitmap *b)
{
	bmp[n]->set(0, 0, bmp[n]->w, bmp[n]->h, 0, 0, b->w, b->h, b, bitmapNORMAL, bitmapNORMAL);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Asnap2::pulse()
{
	if(ctrlPaint)
	{
		repaint();
		ctrlPaint=false;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Asnap2::paint(Abitmap *b)
{
	{
		int	n=0;
		int	x,y;
		for(y=0; y<4; y++)
		{
			int	yy=Y+y*18;
			for(x=0; x<4; x++)
			{
				int	xx=X+x*28;
				b->set(xx, yy, bmp[n], bitmapNORMAL, bitmapNORMAL);
				n++;
			}
		}
	}
	{
		int	x=X+(ipos&3)*28;
		int	y=Y+(ipos>>2)*18;
		b->boxfa(x, y, x+23, y+13, 0xffffff00, 0.3f);
		b->box(x-1, y-1, x+24, y+14, 0xffffff00);
		b->box(x-2, y-2, x+25, y+15, 0xff808000);
	}
	controlPaint(b, pos.w, pos.h);
}

