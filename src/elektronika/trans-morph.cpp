void Atransmorph::settings()
{
	Aeffect *e=Aeffect::getEffect(this);
	int		w=e->getVideoWidth()*2;
	int		h=e->getVideoHeight();
	sources->size(w, h);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Atransmorph::change()
{
	int		n=list->getCurrentData();
	char	*path=dir[n];
	{
		struct _finddata_t		fd;
		char					rpath[ALIB_PATH];
		strcpy(rpath, path);
		strcat(rpath, "\\*.png");
		long					h=_findfirst(rpath, &fd);
		if(h!=-1)
		{
			int		r=0;
			int		nb=0;
			while(r!=-1)
			{
				char		str[ALIB_PATH];
				sprintf(str, "%s\\%s", path, fd.name);
				if(nb<16)
				{
					section.enter();
					bmp[nb]->load(str);
					snap->setBitmap(nb, bmp[nb]);
					nb++;
					section.leave();
				}
				r=_findnext(h, &fd);
			}
			_findclose(h);

			section.enter();
			while(nb<16)
			{
				Abitmap	*b=slice;
				bmp[nb]->size(b->w, b->h);
				bmp[nb]->set(0, 0, b, bitmapNORMAL, bitmapNORMAL);
				snap->setBitmap(nb, bmp[nb]);
				nb++;
			}
			section.leave();
		}
	}
	bChange=true;
}

